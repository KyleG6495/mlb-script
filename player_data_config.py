def load_player_ids_from_file(file_path: str) -> dict:
    """
    Load player ID mappings from CSV or JSON file.
    Expected CSV format: name,player_id
    Expected JSON format: {"name": player_id, ...}
    """
    import pandas as pd
    import json
    from pathlib import Path
    
    file_path = Path(file_path)
    
    if not file_path.exists():
        print(f"⚠️  Player ID file not found: {file_path}")
        return {}
    
    try:
        if file_path.suffix.lower() == '.json':
            with open(file_path, 'r') as f:
                return json.load(f)
        elif file_path.suffix.lower() == '.csv':
            df = pd.read_csv(file_path)
            return dict(zip(df['name'], df['player_id']))
        else:
            print(f"⚠️  Unsupported file format: {file_path.suffix}")
            return {}
    except Exception as e:
        print(f"❌ Error loading player IDs from {file_path}: {e}")
        return {}

def save_player_ids_to_file(player_ids: dict, file_path: str, format: str = 'json'):
    """
    Save player ID mappings to file.
    format: 'json' or 'csv'
    """
    import pandas as pd
    import json
    from pathlib import Path
    
    file_path = Path(file_path)
    file_path.parent.mkdir(parents=True, exist_ok=True)
    
    try:
        if format.lower() == 'json':
            with open(file_path, 'w') as f:
                json.dump(player_ids, f, indent=2)
        elif format.lower() == 'csv':
            df = pd.DataFrame(list(player_ids.items()), columns=['name', 'player_id'])
            df.to_csv(file_path, index=False)
        
        print(f"✅ Saved {len(player_ids)} player IDs to {file_path}")
    except Exception as e:
        print(f"❌ Error saving player IDs to {file_path}: {e}")

